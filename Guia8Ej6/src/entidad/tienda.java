package entidad;

/**
 *
 * @author ezepl
 */
public class tienda {
    String producto;
    double precio;

    public tienda() {
    }

    public tienda(String producto, double precio) {
        this.producto = producto;
        this.precio = precio;
    }

    public String getProducto() {
        return producto;
    }

    public void setProducto(String producto) {
        this.producto = producto;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }














}
